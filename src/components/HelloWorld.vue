<template>
  <div>
    <input type="text" v-model="newTodoText">
    <button @click="addTodo">Add</button>
    <ul>
      <li v-for="(todo, index) in todos" :key="index">{{ todo }}</li>
    </ul>
  </div>
</template>

// Этот блок кода отвечает за отображение пользовательского интерфейса (UI) нашего приложения.
// Мы используем шаблон в формате HTML с директивами Vue.js.

// Директива v-model связывает значение нашего элемента ввода
// с переменной newTodoText.
// Таким образом, когда пользователь вводит текст в поле ввода,
// значение newTodoText автоматически обновляется.

// Директива v-for позволяет нам создавать цикл для каждого
// элемента массива todos и отображать их на странице. Мы также используем
// директиву :key для определения уникального ключа для каждого элемента в цикле.

<script>
export default {
  data() {
    return {
      newTodoText: "",
      todos: [],
    };
  },
  methods: {
    addTodo() {
      if (this.newTodoText.trim().length === 0) {
        return;
      }
      this.todos.push(this.newTodoText);
      this.newTodoText = "";
    },
  },
};
</script>

// Этот блок кода отвечает за логику нашего приложения. Он экспортирует объект,
// который определяет свойства и методы нашего компонента.

// Мы определяем свойства newTodoText и todos, которые хранят текущее
// значение текста для новой задачи и массив всех задач соответственно.

// Мы также определяем метод addTodo, который вызывается при нажатии
// на кнопку "Add". Этот метод проверяет, что текст новой задачи не пустой,
// затем добавляет новую задачу в массив todos и сбрасывает значение newTodoText
// в пустую строку.